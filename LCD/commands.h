#define		CLEAR		0x01 // clear the display & reset the cursor to address 00 H position
#define		RESET		0x03 // return the cursor to address 00 H position
#define		RIGHT_DIR	0x06 // cursor moves to right
#define		LEFT_DIR	0x04 // cursor moves to left
#define		TURN_OFF	0x08 // turn off the display
#define		TURN_ON		0x0F // turn on the display & open cursor & cursor flashing